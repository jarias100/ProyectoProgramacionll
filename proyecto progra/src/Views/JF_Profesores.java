    private void Btn_InsertCursoActionPerformed(java.awt.event.ActionEvent evt, String DNIC, String NISSC, String NombreC, String Apellido1C, String Apellido2C, String DomicilioC, String CiudadC, String ProvinciaC, String CPC, String EmailC, String TelefonoC) {

        System.out.println("Boton de insertar");
        char DNI;
        char NISS;
        char Nombre;
        char Apellido1;
        char Apellido2;
        char Domicilio;
        char Ciudad;
        char Provincia;
        char CP;
        char Email;
        char Telefono;

        char guardado;

        DNIC = jT_DNI.getText().toString();
        NISSC = jT_NISS.getText().toString();
        NombreC = jT_Nombre.getText().toString();
        Apellido1C = jT_Apellido1.getText().toString();
        Apellido2C = jT_Apellido2.getText().toString();
        DomicilioC = jT_Domicilio.getText().toString();
        CiudadC = jT_Ciudad.getText().toString();
        ProvinciaC = jT_Provincia.getText().toString();
        CPC = jT_CP.getText().toString();
        EmailC = jT_Email.getText().toString();
        TelefonoC = jT_Telefono.getText().toString();

        ProfesoresC regProfesores = new ProfesoresC();

        try {
            guardado = regProfesores.RegistrarProfesores(DNIC, NISSC, NombreC, Apellido1C, Apellido2C, DomicilioC, CiudadC, ProvinciaC, CPC, EmailC, TelefonoC);

            if (guardado == 0) {
                JOptionPane.showMessageDialog(null, "No se guardo el registro");
            } else {
                JOptionPane.showMessageDialog(null, "Registro guardado correctamente");
            }
        } catch (SQLException ex) {
            Logger.getLogger(JF_Profesores.class.getName()).log(Level.SEVERE, null, ex);
        }
        LimpiarTxt();
        LimpiarTabla();
        LlenarTabla();
    }

