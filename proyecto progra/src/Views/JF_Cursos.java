    private void Btn_InsertCursoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_InsertCursoActionPerformed

        System.out.println("Boton de insertar");
        String DenominacionC;
        int HorasC;
        String ModalidadC;
        String ObjetivosC;
        String ContenidosC;
        int guardado;

        DenominacionC = JT_Denominacion.getText().toString();
        HorasC = Integer.parseInt(JT_Horas.getText().toString());
        ModalidadC = JT_Modalidad.getText().toString();
        ObjetivosC = JT_Objetivos.getText().toString();
        ContenidosC = Jt_Contenidos.getText().toString();

        CursosC regCurso = new CursosC();

        try {
            guardado = regCurso.RegistrarCurso(DenominacionC, HorasC, ModalidadC, ObjetivosC, ContenidosC);

            if (guardado == 0) {
                JOptionPane.showMessageDialog(null, "No se guardo el registro");
            } else {
                JOptionPane.showMessageDialog(null, "Registro guardado correctamente");
            }
        } catch (SQLException ex) {
            Logger.getLogger(JF_Cursos.class.getName()).log(Level.SEVERE, null, ex);
        }
        LimpiarTxt();
        LimpiarTabla();
        LlenarTabla();
    }//GEN-LAST:event_Btn_InsertCursoActionPerformed

