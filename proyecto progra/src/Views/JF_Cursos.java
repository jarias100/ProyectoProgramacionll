    public void LimpiarTabla() {
        for (int i = 0; i < tbl_TablaCursos.getRowCount(); i++) {
            DefaultTableModel modelo = (DefaultTableModel) tbl_TablaCursos.getModel();
            modelo.removeRow(i);
            i -= 1;
        }
    }

    public void LlenarTabla() {
        Object[][] curs = null;

        CursosC cur = new CursosC();

        curs = cur.CargarTablaCursos();

        DefaultTableModel modelo2 = (DefaultTableModel) tbl_TablaCursos.getModel();

        for (int i = 0; i < curs.length; i++) {
            modelo2.addRow(curs[i]);
        }

        tbl_TablaCursos.setModel(modelo2);
    }

    public void LimpiarTxt() {
        JT_Curso.enable();
        JT_Denominacion.setText("");
        JT_Horas.setText("");
        JT_Modalidad.setText("");
        JT_Objetivos.setText("");
        Jt_Contenidos.setText("");
    }

    public void MostrarTabla() {
        int HorasC = 0;
        int NumCursoC = 0;
        DefaultTableModel tc = (DefaultTableModel) tbl_TablaCursos.getModel();

        String NumCur = String.valueOf(tc.getValueAt(tbl_TablaCursos.getSelectedRow(), 0));
        String DenomCur = String.valueOf(tc.getValueAt(tbl_TablaCursos.getSelectedRow(), 1));
        String HoraCur = String.valueOf(tc.getValueAt(tbl_TablaCursos.getSelectedRow(), 2));
        String ModaCur = String.valueOf(tc.getValueAt(tbl_TablaCursos.getSelectedRow(), 3));
        String ObjeCur = String.valueOf(tc.getValueAt(tbl_TablaCursos.getSelectedRow(), 4));
        String ContenCur = String.valueOf(tc.getValueAt(tbl_TablaCursos.getSelectedRow(), 5));

        NumCursoC = Integer.parseInt(NumCur);
        HorasC = Integer.parseInt(HoraCur);
        
        String NumeroCurso = Integer.toString(NumCursoC);
        String HorasCurso = Integer.toString(HorasC);
        
        JT_Curso.setText(NumeroCurso);
        JT_Denominacion.setText(DenomCur);
        JT_Horas.setText(HorasCurso);
        JT_Modalidad.setText(ModaCur);
        JT_Objetivos.setText(ObjeCur);
        Jt_Contenidos.setText(ContenCur);

    }

